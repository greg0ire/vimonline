
<span class="sidebarheader">What is A-A-P?</span>
<p>
A-A-P is a tool for developing, distributing, downloading, building and
installing software.
Since the author of Vim is also the project leader for A-A-P, there are a few
connections between the two: A-A-P is used to build and distribute Vim, Vim is
the preferred editor in A-A-P.
<p>
More information about A-A-P can be found
<A HREF="http://www.a-a-p.org">here</A>.

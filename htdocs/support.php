<span class="sidebarheader">Documentation</span>
<p>
A good place to start is by reading through vim help. If you type :help [keyword] in vim
it will show you help on the given keyword. (You can use :help help for more help on help!)
There is also a great <a href="http://physlab.sci.ccny.cuny.edu/~orycc/vim-main.html">5.6 reference guide</a> 
which is worth reading. You should also search <a href="tips/search.php">vim tips</a> 
as well as the <a href="http://groups.yahoo.com/group/vim">vim list archive</a>.

</p>
<span class="sidebarheader">The Book</span>
<p>
There is a <a href="http://www.iccf.nl/click5.html">vim book</a> that is helpful in both picking up vim as well 
as learning advanced concepts.
</p>


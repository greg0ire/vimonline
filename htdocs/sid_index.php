<span class="sidebarheader"><a href="about.php">What is Vim?</a></span>
<p>
Vim is a highly configurable text editor built to enable efficient text
editing. It is an improved version of the vi editor distributed with most UNIX
systems. Vim is distributed free as charityware. If you find Vim a useful
addition to your life please consider
<a href="http://iccf-holland.org/">helping needy children in Uganda</a>.

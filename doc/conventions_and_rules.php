<H1>Follow convention and rules</H1>
<p>
In order to keep development organized, some rules have been adopted.
</p>
<p>
Rules are not law. If you think a rule slow down your development, feel free
to discuss rule validity with the other project members.
</p>

<h2>The rules are the following</h2>
<ul>
<li>put your name and e-mail in the code you change, so we can contact you for
more information.
<li>write simple code or function, so it can esealy be reread by someone else.
<li>in code, use comment efficiently. Set a file header and a function header
(<a href="">See. comment and document</a>).
<li>provide tools to automate complex or repetitive task.
<li>respect project goal.
<li>discuss and share any idea.
</ul>

<h2>Conventions</h2>
<ul>
<li>use a textwidth=78, so source code can be printed esealy.
</ul>

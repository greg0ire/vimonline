<H1>Separated tools and web site</H1>
<p>
vimonline is separated across different web sites:
</p>
<table>
<tr>
<td valign=top><a href="http://vim.sf.net">vim.sourceforge.net</a></td>
<td>production web site where real interaction with users apears</td>
</tr>
<tr>
<td valign=top><a href="http://vimonline.sf.net">vimonline.sourceforge.net</a></td>
<td>development web site where test, crash, data lost can be done</td>
</tr>
<tr>
<td valign=top><a href="http://sf.net/projects/vimonline">sourceforge.net/projects/vimonline</a></td>
<td>development management web site, where developers and vimonline users can
share and manage idea, and more...</td>
</tr>
</table>
<p>
The doamin <tt>sf.net</tt> and <tt>sourceforge.net</tt> are the same.
</p>
<p>
Production web site and development web site use separated files and
databases. They are not writable by the same users.
</p>
<p>
Some development tools are provided on sourceforge. They include sourceforge
Collaborative Development System (CDS) and programmer tools. They are only
available for project members.
</p>
<p>
If you want to become a member, please read <a href="https://sourceforge.net/docman/display_doc.php?docid=9124&group_id=27891">this document</a>.
</p>

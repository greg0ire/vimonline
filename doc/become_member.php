<H1>Become member of the development team</H1>
<p>
To become a member you must first create a sourceforge user, see instructions
<a href="http://sourceforge.net/account/register.php">here</a>.
</p>
<p>
Next you must contact a vimonline project admin by e-mail. Administrator are
listed <a href="http://sf.net/projects/vimonline">here</a>.
</p>
<p>
If an administrator accept your request, you will see your sourceforge
username added to the vimonline members list. It may take a few days.
</p>
<p>
You should receive a confirmation e-mail from the administrator.
</p>
<p>
Next you will be able to access any vimonline development tool, files, shell,
CVS and documentation.
</p>
<p>
See <a href="http://sourceforge.net/docman/display_doc.php?docid=753&group_id=1">sourceforge documentation</a> for more details.
</p>

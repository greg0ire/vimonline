<H1>Follow design</H1>
<p>
We have produced some guidelines to provide easier developer integration. It's
important for all developer to follow this guidelines rigourously to keep our
concept of quality.
</p>
<p>
Guidelines provide easy integration to the development but introduce
complexity and more work for the programmer. 
</p>
<p>
Current design choice may be discussed. Comment and idea are welcome.
</p>
<p>
The current design and web site structure are available for project members
only.
</p>
